package ex_10_For_loop;

public class Lab103 {
    public static void main(String[] args) {
        for (int rohit = 0; rohit <= 10; ++rohit){
            System.out.println(rohit);
        }

        for (int a = 0; a <= 10; a++){
            System.out.println(a);
        }

        for(int _1 = 0; _1 <= 10; _1++){
            System.out.println(_1);
        }

    }
}
