package ex_04_Operators;

public class Lab37_Operators_Arithmetic_operators {
    public static void main(String[] args) {

        // Arithmetic operator
        // + addition
        // - subtraction
        // * multiplication
        // / division
        // % modulo | Modulus - remainder

        int a = 20;
        int b = 3;
        float c = 3f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); //6
        System.out.println(a/c); // 6.666665


    }
}
