package ex_04_Operators;

public class Lab45 {
    public static void main(String[] args) {
        // == ? compare the values
        System.out.println(10 == 10);
        System.out.println(10 < 10);
        System.out.println(10 == 20);
        System.out.println(10 <= 10);
        // 10 < 10 or 10 = 10
        System.out.println(10 >= 10); // 10 > 10 or 10 = 10
    }
}
